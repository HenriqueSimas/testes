class Campos {
    constructor(inputTitle, inputDateL, inputDesc) {
        this.inputTitle = inputTitle;
        this.inputDateL = '';
        this.inputDesc = '';
        this.inputTitle = inputTitle;
        if (inputDateL)
            this.inputDateL = inputDateL;
        if (inputDesc)
            this.inputDesc = inputDesc;
    }
}
